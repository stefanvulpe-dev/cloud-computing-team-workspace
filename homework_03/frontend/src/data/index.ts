export function getRecipeTags(): string[] {
  const tags = new Set<string>();
  for (let i = 0; i < 3; ++i) {
    const tag = recipeTags[Math.floor(Math.random() * recipeTags.length)];
    tags.add(tag);
  }
  return Array.from(tags);
}
